.MODEL SMALL



.STACK 100H



.CODE
MAIN PROC
;INITIALIZE COUNTER AND DL REGISTER
MOV CX, 53
MOV DL, 'A'
;PRINT ALL THE CHARACTERS IN ASCII/KBD
MOV AH, 2 ;SINGLE CHAR OUTPUT



PRINT_LOOP:
INT 21H
INC DL
;compare if DL is over the small z
;if so, then DL must be reset with Z
DEC CX
JNZ PRINT_LOOP

;DOS EXTI ROUTINE
MOV AX, 4CH
INT 21H
MAIN ENDP

END MAIN